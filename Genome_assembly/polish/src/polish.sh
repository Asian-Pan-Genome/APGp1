cd mapping
if [ -e mapping.finish ]; then
	# pass
	echo "mapping already done"
else
	if [ ! -s $wk/repetitive_k15.txt ];then
		meryl count k=15 output merylDB ${draft}
		meryl print greater-than distinct=0.9998 merylDB > repetitive_k15.txt && rm -r merylDB
	fi

	sh $src/ont_mapping.sh  ${id} ${hap} ${draft}
	sh $src/hifi_mapping_filt.sh  ${id} ${hap} ${draft}
	if [ -s ont_mapping.sh ] && [ -s hifi_mapping.sh ];then
		$SJM -t 300 -m 50g -c 48 ont_mapping.sh hifi_mapping.sh
	else
		echo "can not find one of mapping shells, please check"
		exit 1
	fi
	if [ -s ont2asm.ont.sort.filt_clip.bam.bai ] && [ -s hifi2asm.hifi.sort.filt_clip.bam.bai ];then
		touch mapping.finish
	fi
fi

cd ..
###########################################################
#################### SV #############################
###########################################################
