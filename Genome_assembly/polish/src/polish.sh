echo "[INFO]: $hap"
scaf=$(realpath $draft)
echo "[INFO]: scaf is: $scaf"

# CONFIGURE OF DATA, SOFTWARE..
if [ -s $src/init.conf  ]; then
	echo "Load configure from $src/init.conf"
	source $src/init.conf
else
	echo "Can not assess to init.conf in $src"
	exit -1
fi

echo "[INFO]: DATA is $DATA"
