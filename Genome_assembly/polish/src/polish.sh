	# pass
	echo "svPolish already done"
else
	sh $src/svPolish_shell.sh ${id} ${hap} $wk/../mapping/hifi2asm.hifi.sort.filt_clip.bam $wk/../mapping/ont2asm.ont.sort.filt_clip.bam $draft
	if [ -s svPolish.sh  ];then
		$SJM -t 300 -m 30g -c 16 svPolish.sh
	else
		echo "can not find svPolish.sh, please check"
		exit -1
	fi
	if [ -s Iris_jasmine.hifi_ont.sniffles.polished.fasta ]; then
		touch svPolish.finish
	fi

fi
